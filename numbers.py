# int
n = 34
# Different printing ways
print(f"n={n}")
print("n=" + str(n))

# float
m = 3.14
print(f"m={m}")
print("m=" + str(m))

# Basic numerical operations

x = 3
print(x)

# add
y = x + 3
print("Add: " + str(y))

# subtract
y = x - 1
print("Subtract: " + str(y))

# multiply (Can multiply int with float in python | Casting, converting from one data type to other automatically)
y = x * 7.855
print("Multiply: " + str(y))

# divide (Can multiply int with float in python | Casting, converting from one data type to other automatically)
y = x / 0.5
print("Divide: " + str(y))

# power
y = x**2
print("Power: " + str(y))

# reminder / modulus
y = x % 2
print("Mod: " + str(y))
